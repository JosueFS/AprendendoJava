    public void calcular(){
        switch(calc){
            case 1: //Somar
                ans = num + Double.parseDouble(visor.getText());
                visor.setText(Double.toString(ans));
                break;
            case 2: //Subtrair
                ans = num - Double.parseDouble(visor.getText());
                visor.setText(Double.toString(ans));
                break;
            case 3: //Multiplicar
                ans = num * Double.parseDouble(visor.getText());
                visor.setText(Double.toString(ans));
                break;
            case 4: //Dividir
                ans = num / Double.parseDouble(visor.getText());
                visor.setText(Double.toString(ans));
                break;
        }
    }

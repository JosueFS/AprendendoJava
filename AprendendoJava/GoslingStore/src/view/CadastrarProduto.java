    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        Produto p = new Produto();
        ProdutoDAO dao = new ProdutoDAO();
        p.setId(Integer.parseInt(txtCod.getText()));
        
        dao.delete(p);
        //System.out.println("Linha selecionada " + tbProdutos.getSelectedRow());
         try{
            DefaultTableModel dtmProdutos = (DefaultTableModel) tbProdutos.getModel();
            dtmProdutos.removeRow(this.tbProdutos.getSelectedRow());
        }catch (Exception e){
            JOptionPane.showMessageDialog(null,"Nenhuma linha selecionada!");
        }
         

        
    }//GEN-LAST:event_btExcluirActionPerformed

    private void tbProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbProdutosMouseClicked
        if(tbProdutos.getSelectedRow() != -1){
            this.txtCod.setText(tbProdutos.getValueAt(tbProdutos.getSelectedRow(), 0).toString());
            this.txtDesc.setText(tbProdutos.getValueAt(tbProdutos.getSelectedRow(), 1).toString());
            this.txtQtd.setText(tbProdutos.getValueAt(tbProdutos.getSelectedRow(), 2).toString());
            this.txtPreco.setText(tbProdutos.getValueAt(tbProdutos.getSelectedRow(), 3).toString());
        }
    }//GEN-LAST:event_tbProdutosMouseClicked

    private void tbProdutosKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tbProdutosKeyReleased
        if(tbProdutos.getSelectedRow() != -1){
            this.txtCod.setText(tbProdutos.getValueAt(tbProdutos.getSelectedRow(), 0).toString());
            this.txtDesc.setText(tbProdutos.getValueAt(tbProdutos.getSelectedRow(), 1).toString());
            this.txtQtd.setText(tbProdutos.getValueAt(tbProdutos.getSelectedRow(), 2).toString());
            this.txtPreco.setText(tbProdutos.getValueAt(tbProdutos.getSelectedRow(), 3).toString());
        }
    }//GEN-LAST:event_tbProdutosKeyReleased

    private void btUpdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btUpdActionPerformed
        try {
            this.tbProdutos.setValueAt(txtCod.getText(), tbProdutos.getSelectedRow(), 0);
            this.tbProdutos.setValueAt(txtDesc.getText(), tbProdutos.getSelectedRow(), 1);
            this.tbProdutos.setValueAt(txtQtd.getText(), tbProdutos.getSelectedRow(), 2);
            this.tbProdutos.setValueAt(txtPreco.getText(), tbProdutos.getSelectedRow(), 3);
        }catch (Exception e){
            JOptionPane.showMessageDialog(null,"Nenhuma linha selecionada");
        }
        Produto p = new Produto();
        ProdutoDAO dao = new ProdutoDAO();
        p.setDescricao(txtDesc.getText());
        p.setQtd(Integer.parseInt(txtQtd.getText()));
        p.setPreco(Double.parseDouble(txtPreco.getText()));
        p.setId(Integer.parseInt(txtCod.getText()));
        
        dao.update(p);
    }//GEN-LAST:event_btUpdActionPerformed

